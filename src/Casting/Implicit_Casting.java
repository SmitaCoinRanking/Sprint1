package Casting;

public class Implicit_Casting {
	public static void main(String[] args) {
		//Implicit casting
		double d =10;
		System.out.println(".....Implicit casting.....");
		System.out.println(d);
		
		//Explicit casting
		int i =(int)10.8;
		System.out.println("....Explicit casting....");
		System.out.println(i);
	}

}
